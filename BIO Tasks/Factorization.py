import math
n = int(input("n = "))
"""
Program outputs the prime factorization of n as a list. We will look for primes less or equal n, 
using the Seive of Erathostenes. Once we find a prime number p, we check whether n is a multiple of p.
If it is, we keep appending p to the factorization list and dividing n by p, reducing it as long as possible. 
The program stops, when n reaches 1.
"""
factorization = [] #output

L = [l for l in range(2, n+1)]

while n>1:














































































































































































































































































    
    p = L[0] #the first element of L is a prime
    L = [l for l in L if l%p!=0 and l<=n] #we remove multiples of p from L and numbers smaller than our current n

    while n%p == 0:
        factorization += [p]
        n = n//p

print(factorization)